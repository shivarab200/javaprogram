package datatypes;

public class AreaOfCircle {

	public static void main(String[] args) 
	{
		//Declaration
		float pi;
		int rad;
		float Area;
		
		//Assigning values
		pi=3.14f;
		rad=4;
		Area=pi*(rad*rad);
		
		//Printing the value
		System.out.println("The area of the circle for the radious of"+rad+" is "+Area);

	}

}
