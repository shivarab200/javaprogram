package datatypes;

public class AreaOfRectangle 
{
	public static void main(String[] args)
	{
		int len;
		int bre;
		
		//Assign values
		
		len=4;
		bre=6;
		
		//finding area
		int Area=len*bre;
		
		//printing the area
		System.out.println("The area of the rectangle size of "+len+" length "+bre+" breadth is "+Area);
		
	}
}
