package array;

public class CreationOfArray {

	public static void main(String[] args) 
	{
		char[] ch=new char[5];
		ch[0]='S';
		ch[1]='h';
		ch[2]='i';
		ch[3]='v';
		ch[4]='a';
		
		System.out.println(ch[0]);
		System.out.println(ch[1]);
		System.out.println(ch[2]);
		System.out.println(ch[3]);
		System.out.println(ch[4]);
		
		System.out.println(ch);
	}

}
