package datatypes;

public class DataTypes 
{
	public static void main(String[] args)
	{
		int intval;
		long longval;
		float flval;
		double dval;
		boolean boolval;
		short shval;
		byte byval;
		char cval;
		
		intval = 542;
		longval = 2261l;
		flval = 123.1f;
		dval = 122.12;
		boolval = true;
		shval = 12;
		byval =2;
		cval ='s';
		System.out.println("The value of int"+" "+intval);
		System.out.println("The value of long"+" "+longval);
		System.out.println("The value of float"+" "+flval);
		System.out.println("The value of double"+" "+dval);
		System.out.println("The value of boolean"+" "+boolval);
		System.out.println("The value of short"+" "+shval);
		System.out.println("The value of byte"+" "+byval);
		System.out.println("The value of char"+" "+cval);
		
	}
}
